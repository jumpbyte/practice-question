    class Solution {

        public TreeNode constructMaximumBinaryTree(int[] nums) {
            if (nums == null || nums.length == 0) {
                return null;
            }
            return build(nums, 0, nums.length - 1);
        }

        /**
         * å®šä¹‰ï¼š æ„å»ºä¸€æ£µæ ‘ï¼Œä»¥numsæ•°ç»„startå’Œendä¹‹é—´æœ€å¤§çš„æ•°ä¸ºæ ¹ï¼Œå°†å…¶å·¦å³éƒ¨åˆ†åˆ†åˆ«ä½œä¸ºå·¦å³å­æ ‘ï¼Œè¿”å›æ ¹èŠ‚ç‚¹
         * @param nums
         * @param start
         * @param end
         * @return
         */
        public TreeNode build(int[] nums, int start, int end) {
            if (start > end ) {
                return null;
            }
            int maxIndex = max(nums, start, end);
            TreeNode root = new TreeNode(nums[maxIndex]);
            //é€’å½’æ„å»ºå·¦å­æ ‘ã€å³å­æ ‘
            root.left = build(nums, start, maxIndex - 1);
            root.right = build(nums, maxIndex + 1, end);
            return root;
        }

        /**
         * è¿”å›nums[start - end]åŒºé—´æœ€å¤§å€¼çš„ä¸‹æ ‡ç´¢å¼•
         * @param nums
         * @param start
         * @param end
         * @return
         */
        private int max(int[] nums, int start, int end) {
            int maxI = start;
            int maxNum = nums[start];
            for (int i = start + 1; i <= end; i++) {
                if (nums[i] > maxNum) {
                    maxNum = nums[i];
                    maxI = i;
                }
            }
            return maxI;
        }

    }
